def moment_solver_timeevolve(L0: QGsuper = None,
                             Lt: list[tuple[QGsuper,typing.Callable[[float], complex]]] = [],
                             rho0: QGstate = None,
                             tlist: list[float] | npt.NDArray[float] = [0,1],
                             **options
                             ):
    """
    ---- Procedure ----
    Time-dependent solver for the steady-state for a corresponding CV system Liouvillian, which may be time-dependent. 
    The function will output the CV system state at every time specified in tlist. In case the total dynamics is that 
    of a true-quantum state (CPTP) then it is best that each QGsuper also be CPTP. This check is currently performed on
    each element of LT, and will influence the choice of solver. If the total dynamics is not CPTP, then the covariance, 
    means, and norm must all be solved simultaneously, increasing the number of variables that must be handled by the 
    ODE solver. As an example, the following input will result in the non-norm preserving solver being called since the
    ladder operators are not-Hermitian, even though together it is just the dynamics of a Hermitian Hamiltonian:
        LT = [[coherent(create()) , lambda t: np.exp(1j*np.pi*t/2)] , 
              [coherent(destroy()) , lambda t: np.exp(-1j*np.pi*t/2)]].
    The following equivalent statement has no such issue since the QGopers are Hermitian:
        LT = [[coherent(position()) , lambda t: np.sqrt(2)*np.cos(np.pi*t/2)] , 
              [coherent(momentum()) , lambda t: np.sqrt(2)*np.sin(np.pi*t/2)]]
           = [[coherent(create() + destroy()) , lambda t: np.cos(np.pi*t/2)] , 
              [coherent(1j*(create() - destroy())) , lambda t: np.sin(np.pi*t/2)]],

    ---- Parameters ----
    LO : QGsuper
        Time-independent component of the total Lindbladian/Liouvillian. The Liouvillian need not describe the 
        evolution of a true master equation. Must be CVS-only, and so contain no FLS component.
    Lt : list[tuple[QGsuper,function]]
        The time-dependent component(s) of the total Lindbladian/Liouvillian. Must be passed as a list of two-element
        lists or tuples, where the first element is a QGsuper and the section is the function corresponding to the
        time-dependent coefficient. The function should therefore only accept one argument for the time.
    rho0 : QGstate
        Initial state for the system. Must be a CVS-only state.
    tlist : list[float]
        List of time values at which to store solutions from the state evolution solver. This must be sorted, and
        should contain the start and end times. The default initial and final times are t=[0,1].
    tol : float
        Set tolerance for the magnitude of real or imaginary parts of numbers. Parts of numbers below this
        tolerance value are set to zero.
    **options : 
        Options to be passed to the ODE solver. All options are listed below.
    atol : float
        Absolute tolerance, default value is settings.atol.
    rtol : float
        Relative tolerance, default value is settings.rtol.
    method : str
        Integration method to be used by Scipy solve_ivp. Due to the data used here, only solvers which can handle
        complex values will work. The default is ‘RK45’. Other choices for explicit methods are ‘RK23’ and ‘DOP853’, 
        while for implicit solvers the choice is ‘BDF’.

    ---- Returns ----
    rhot : list[QGstate]
        List of QGstates at the times indicated in tlist.
    """
    # Set options
    defaults = {'atol': qgauss.settings.atol, 'rtol': qgauss.settings.rtol, 'method': 'RK45'}
    options = {**defaults, **options}
    tol = options['atol']

    # Extract data from the initial state
    dims = rho0.dims_cvs
    V0 = symmat_to_vec(rho0.data_2nd)
    m0 = rho0.data_1st
    n0 = rho0.data_0th

    # Extract arrays for the Fokker-Planck equation in Wigner phase-space from L0 and LT. For the time-dependent
    # component, only save those which are non-zero.
    if L0 == None:
        A0, B0, C0 = np.zeros(rho0.shape_2nd), np.zeros(rho0.shape_2nd), np.zeros(rho0.shape_2nd)
        D0, F0 = np.zeros(rho0.shape_1st), np.zeros(rho0.shape_1st)
        G0 = np.zeros(rho0.shape_0th)
    else:
        A0 = L0.wigner_2nd_deriv_var
        B0 = L0.wigner_2nd_var
        C0 = L0.wigner_2nd_deriv
        D0 = L0.wigner_1st_var
        F0 = L0.wigner_1st_deriv
        G0 = L0.wigner_0th

    if Lt == []:
        At, Bt, Ct, Dt, Ft, Gt = [], [], [], [], [], []
    else:
        At = [x for x in Lt if np.all(np.abs(x[0].wigner_2nd_deriv_var) < tol)]
        Bt = [x for x in Lt if np.all(np.abs(x[0].wigner_2nd_var) < tol)]
        Ct = [x for x in Lt if np.all(np.abs(x[0].wigner_2nd_deriv) < tol)]
        Dt = [x for x in Lt if np.all(np.abs(x[0].wigner_1st_var) < tol)]
        Ft = [x for x in Lt if np.all(np.abs(x[0].wigner_1st_deriv) < tol)]
        Gt = [x for x in Lt if np.all(np.abs(x[0].wigner_0th) < tol)]

    # Check if dynamics of the covariances and means do not preverse the norm of the state. Pure dissipation is allowed
    # in the form of a non-zer Gt. The first section will solve the ODEs when the covariances and means are uncoupled, 
    # and the second case will solve them when they are coupled, and hence need to be handled simultaneously.
    if (np.all(np.abs(B0) < tol) 
        and np.all(np.abs(D0) < tol)
        and len(Bt) == 0
        and len(Dt) == 0
        ):
        # Define functions to pass to solve_ivp
        def _ode_func_cov(t,X):
            # Function for the covariance to pass to solve_ivp, which requires vectorization of the symmetric matrix.
            #   dΣ(t)/dt = A.Σ(t) + Σ(t).A^T + C
            A = A0 + sum([x[0]*x[1](t) for x in At])
            C = C0 + sum([x[0]*x[1](t) for x in Ct])
            V = vec_to_symmat(X, 2*dims)
            return symmat_to_vec(A@V + V@np.transpose(A) + C)
        
        def _ode_func_mean(t,X):
            # Function for the means to pass to solve_ivp.
            #   dμ(t)/dt = A.μ(t) + F
            A = A0 + sum([x[0]*x[1](t) for x in At])
            F = F0 + sum([x[0]*x[1](t) for x in Ft])
            return A@X + F
        
        def _ode_func_norm(t,X):
            # Function for the means to pass to solve_ivp. Function is determined by solving
            #   dn(t)/dt = d/dt exp(-v(t)) = exp(-v(t))*(-dv(t)/dt) = -n(t)*G
            G = G0 + sum([x[0]*x[1](t) for x in Gt])
            return -X*G
        
        Vt = solve_ivp(_ode_func_cov, (tlist[0],tlist[-1]), V0, t_eval = tlist, **options)
        mt = solve_ivp(_ode_func_mean, (tlist[0],tlist[-1]), m0, t_eval = tlist, **options)
        nt = solve_ivp(_ode_func_norm, (tlist[0],tlist[-1]), n0, t_eval = tlist, **options)
    
    else:
        # Define single function to pass to solve_ivp which incorporates the ODEs for all moments. The differential
        # equations are defined as:
        #   dΣ(t)/dt = A.Σ(t) + Σ(t).A^T - Σ(t).B.Σ(t) + C
        #   dμ(t)/dt = (A - B.Σ(t)).μ(t) + F - Σ(t).D
        #   dn(t)/dt = -n(t)*(g + f.μ(t) + ½*μ(t).B.μ(t) + ½*tr[B.Σ(t)])
        def _ode_func_total(t,X):
            A = A0 + sum([x[0]*x[1](t) for x in At])
            B = B0 + sum([x[0]*x[1](t) for x in Bt])
            C = C0 + sum([x[0]*x[1](t) for x in Ct])
            F = F0 + sum([x[0]*x[1](t) for x in Ft])
            D = D0 + sum([x[0]*x[1](t) for x in Dt])
            G = G0 + sum([x[0]*x[1](t) for x in Gt])
            # Split X into leading covariance matrix elements and trailing elements for the mean.
            XV = vec_to_symmat(X[0:dims*(2*dims+1)], 2*dims)
            Xm = X[dims*(2*dims+1):dims*(2*dims+3)]
            Xn = X[dims*(2*dims+3)]
            # Perform matrix/vector operations, convert symmetric covariance matrix back to vector, and append the means.
            return np.concatenate((symmat_to_vec(A@XV + XV@np.transpose(A) - XV@B@XV + C),
                                   np.dot(A - XV@B, Xm) + F - XV@D,
                                   -Xn*(G + F@Xm + 0.5*Xm@B@Xm + 0.5*np.trace(B@XV))))

        X0 = np.concatenate((V0,m0,n0))
        Xt = solve_ivp(_ode_func_total, (tlist[0],tlist[-1]), X0, t_eval = tlist, **options)
        
        Vt = [x[0:dims*(2*dims+1)] for x in Xt]
        mt = [x[dims*(2*dims+1):dims*(2*dims+3)] for x in Xt]
        nt = [x[dims*(2*dims+3)] for x in Xt]

    rhot = [QGstate(data_2nd = vec_to_symmat(Vt[t], 2*dims),
                    data_1st = mt[t],
                    data_0th = nt[t],
                    dims_cvs = dims)
                    for t in range(0,len(tlist))]
    return rhot